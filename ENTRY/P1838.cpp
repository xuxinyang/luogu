#include <bits/stdc++.h>
using namespace std;
string s;
int a[5][5], xiaoa, uim;
int main()
{
    cin >> s;
    for (int i = 0; i < s.length(); i++)
    {
        int row = ((s[i] - '0') + 2) / 3;
        int col = ((s[i] - '0') + 2) % 3 + 1;
        if (i % 2 == 0) // 第一个玩家
        {
            a[row][col] = 1;
        }
        else
        {
            a[row][col] = 2;
        }
    }

    if (
        (a[1][1] == 1 && a[1][2] == 1 && a[1][3] == 1) ||
        (a[2][1] == 1 && a[2][2] == 1 && a[2][3] == 1) ||
        (a[3][1] == 1 && a[3][2] == 1 && a[3][3] == 1) ||
        (a[1][1] == 1 && a[2][1] == 1 && a[3][1] == 1) ||
        (a[1][2] == 1 && a[2][2] == 1 && a[3][2] == 1) ||
        (a[1][3] == 1 && a[2][3] == 1 && a[3][3] == 1) ||
        (a[1][1] == 1 && a[2][2] == 1 && a[3][3] == 1) ||
        (a[1][3] == 1 && a[2][2] == 1 && a[3][1] == 1)
    )
    {
        xiaoa = 1;
    }
    if (
        (a[1][1] == 2 && a[1][2] == 2 && a[1][3] == 2) ||
        (a[2][1] == 2 && a[2][2] == 2 && a[2][3] == 2) ||
        (a[3][1] == 2 && a[3][2] == 2 && a[3][3] == 2) ||
        (a[1][1] == 2 && a[2][1] == 2 && a[3][1] == 2) ||
        (a[1][2] == 2 && a[2][2] == 2 && a[3][2] == 2) ||
        (a[1][3] == 2 && a[2][3] == 2 && a[3][3] == 2) ||
        (a[1][1] == 2 && a[2][2] == 2 && a[3][3] == 2) ||
        (a[1][3] == 2 && a[2][2] == 2 && a[3][1] == 2)
    )
    {
        uim = 1;
    }
    if (xiaoa == 1) cout << "xiaoa wins.\n";
    else if (uim == 1) cout << "uim wins.\n";
    else cout << "drew.\n";
    return 0;
}